float normalize_day(int day){
    return (day - 1)/364.0f;
}


float predict_NextHour(float temps[6], float hour_norm, float day_norm) {
    //get Temp difference
    float delta[5];
    for (int i = 0; i < 5; i++)
        delta[i] = (temps[i+1] - temps[i] - delta_mean) / delta_std;

    //inputs[7] = [delta0, delta1,..,..,..,normalized hour, normalized dat]
    float input[7];
    for (int i = 0; i < 5; i++) input[i] = delta[i];
    input[5] = hour_norm;
    input[6] = day_norm;

    //hidden layer
    float hidden[10];
    for (int j = 0; j < 10; j++) {
        hidden[j] = nn_B1[j];
        for (int i = 0; i < 7; i++)
            hidden[j] += input[i] * nn_W1[i][j];
        if (hidden[j] < 0) hidden[j] = 0; //ReLU, out = (value>0)? value : 0;
    }

    //output
    float out = nn_B2;
    for (int j = 0; j < 10; j++)
        out += hidden[j] * nn_W2[j];

    //unnormalize data
    return out * y_std + y_mean;
}